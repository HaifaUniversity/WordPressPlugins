    public function duplicate_staff_post() {
        if (!isset($_GET['post'])) wp_die('לא נבחר פוסט');
        $post_id = absint($_GET['post']);
        check_admin_referer('duplicate_staff_' . $post_id);
        if (!current_user_can('edit_posts')) wp_die('אין הרשאה');
        $post = get_post($post_id);
        if (!$post || $post->post_type !== 'staff') wp_die('פוסט לא תקין');

        $new_id = wp_insert_post([
            'post_title'   => $post->post_title . ' (עותק)',
            'post_content' => $post->post_content,
            'post_status'  => 'draft',
            'post_type'    => 'staff',
            'post_author'  => get_current_user_id(),
        ]);
        if (is_wp_error($new_id)) wp_die('שגיאה בשכפול');

        // Copy all post meta, skipping internal WP keys.
        $all_meta = get_post_meta($post_id);
        foreach ($all_meta as $key => $values) {
            if (str_starts_with($key, '_')) continue;
            foreach ($values as $val) {
                add_post_meta($new_id, $key, maybe_unserialize($val));
            }
        }

        wp_redirect(admin_url('post.php?action=edit&post=' . $new_id));
        exit;
    }

