        $email      = get_post_meta($post_id, 'email',      true);
        $phone      = get_post_meta($post_id, 'external_phone', true);
        $about      = get_post_meta($post_id, 'about',      true);
        $img_id     = get_post_meta($post_id, 'staff_image', true);
        $staff_type = get_post_meta($post_id, 'staff_type',  true);

        $schema = [
            '@context' => 'https://schema.org',
            '@type'    => 'Person',
            'name'     => trim($first_name . ' ' . $last_name),
            'url'      => get_permalink($post_id),
        ];
        if ($first_name) $schema['givenName']   = $first_name;
        if ($last_name)  $schema['familyName']  = $last_name;
        if ($email)      $schema['email']       = $email;
        if ($phone)      $schema['telephone']   = $phone;
        if ($about)      $schema['description'] = wp_strip_all_tags(substr($about, 0, 300));
        if ($img_id)     $schema['image']       = wp_get_attachment_image_url((int)$img_id, 'full') ?: '';

        if (is_array($staff_type) && in_array('academic', $staff_type, true)) {
            $schema['affiliation'] = ['@type' => 'Organization', 'name' => 'University of Haifa', 'url' => 'https://www.haifa.ac.il'];
            $schema['jobTitle']    = 'Academic Staff';
        }

        echo '<script type="application/ld+json">' . "\n";
        echo wp_json_encode($schema, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
        echo "\n" . '</script>' . "\n";
    }

