// ── Staff page <title> fix ────────────────────────────────────────────────────
// WP, Yoast, and Rank Math all need the staff member's name in the title tag.
// We hook all three with priority 99 to override whatever they set.

function haifa_v2_get_staff_title_from_url(): string {
    $path  = trim(parse_url($_SERVER['REQUEST_URI'] ?? '', PHP_URL_PATH), '/');
    $parts = explode('/', $path);
    $slug  = end($parts);
    if (empty($slug)) return '';

    $post  = get_page_by_path($slug, OBJECT, 'staff');
    if (!$post) return '';

    $first = get_post_meta($post->ID, 'first_name', true);
    $last  = get_post_meta($post->ID, 'last_name',  true);
    return trim($first . ' ' . $last);
}

add_filter('pre_get_document_title', function($title) {
    $name = haifa_v2_get_staff_title_from_url();
    return $name ? $name . ' - ' . get_bloginfo('name') : $title;
}, 99);

add_filter('wpseo_title', function($title) {
    $name = haifa_v2_get_staff_title_from_url();
    return $name ? $name . ' - ' . get_bloginfo('name') : $title;
}, 99);

add_filter('rank_math/frontend/title', function($title) {
