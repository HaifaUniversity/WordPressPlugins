/**
 * Read a staff meta field. Mirrors ACF get_field() for simple scalar fields.
 * For staff_image returns the attachment ID (integer).
 * For staff_type / staff_academic_type returns the stored value
 * (WP auto-unserializes arrays on get).
 */
function haifa_v2_get_field(string $name, $post_id = null) {
    if (!$post_id) $post_id = get_the_ID();
    return get_post_meta($post_id, $name, true);
}

/**
 * Return all socials rows for a post as [ ['platform'=>..,'url'=>..], ... ]
 */
function haifa_v2_get_socials($post_id = null): array {
    if (!$post_id) $post_id = get_the_ID();
    $count = (int) get_post_meta($post_id, 'socials', true);
    $rows  = [];
    for ($i = 0; $i < $count; $i++) {
        $platform = get_post_meta($post_id, "socials_{$i}_platform", true);
        $url      = get_post_meta($post_id, "socials_{$i}_url",      true);
        if ($platform && $url) $rows[] = ['platform' => $platform, 'url' => $url];
    }
    return $rows;
}

/**
 * Resolve title_main key → Hebrew display label.
 * Single source of truth — all files must delegate to this function.
 * Handles v2 keys, legacy v1 keys, and custom title options stored in wp_options.
 */
function haifa_v2_title_label(string $value): string {
    $map = [
        // v2 keys
        'mr'    => 'מר',
        'mrs'   => "גב'",
        'dr'    => 'ד"ר',
        'prof'  => "פרופ'",
        'other' => 'אחר',
        // v1 fallback keys (legacy migration)
        'professor'           => 'מר',
        'associate_professor' => "גב'",
        'assistant_professor' => 'ד"ר',
        'lecturer'            => "פרופ'",
    ];
    if (isset($map[$value])) return $map[$value];
    $custom = json_decode(get_option('haifa_staff_custom_title_main', '[]'), true);
    if (is_array($custom)) {
        foreach ($custom as $opt) {
            if (($opt['value'] ?? '') === $value) return $opt['label'];
        }
    }
    return $value;
}

