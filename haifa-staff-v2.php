// [staff_link id="42"]Name[/staff_link]
// Renders a permalink to a staff profile. Used by the @mention system so links
// survive future slug changes (ID-based, not slug-based).
add_shortcode('staff_link', function($atts, $content) {
    $atts = shortcode_atts(['id' => 0], $atts);
    $id   = absint($atts['id']);
    if (!$id) return esc_html($content);
