// AJAX: returns up to 10 staff matching the typed search term.
add_action('wp_ajax_haifa_staff_mention_search', function() {
    check_ajax_referer('haifa_staff_mention', 'nonce');
    $term      = sanitize_text_field($_GET['term'] ?? '');
    $title_map = ['mr' => 'מר', 'mrs' => "גב'", 'dr' => 'ד"ר', 'prof' => "פרופ'"];
    $posts     = get_posts([
        'post_type'      => 'staff',
        'posts_per_page' => 10,
        'post_status'    => 'publish',
        'meta_key'       => 'last_name',
        'orderby'        => 'meta_value',
        'order'          => 'ASC',
        'meta_query'     => [
            'relation' => 'OR',
            ['key' => 'first_name', 'value' => $term, 'compare' => 'LIKE'],
            ['key' => 'last_name',  'value' => $term, 'compare' => 'LIKE'],
        ],
    ]);
    $results = [];
    foreach ($posts as $p) {
        $fn      = get_post_meta($p->ID, 'first_name',  true);
        $ln      = get_post_meta($p->ID, 'last_name',   true);
        $tm      = get_post_meta($p->ID, 'title_main',  true);
        $to      = get_post_meta($p->ID, 'title_other', true);
        $tl      = ($tm === 'other' && $to) ? $to : ($title_map[$tm] ?? '');
        $display = trim(($tl ? $tl . ' ' : '') . $fn . ' ' . $ln);
        $results[] = ['id' => $p->ID, 'name' => $display, 'url' => get_permalink($p->ID)];
    }
    wp_send_json($results);
});

