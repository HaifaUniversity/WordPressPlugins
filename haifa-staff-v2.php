// ── Auto-updater ──────────────────────────────────────────────────────────────
// Checks a remote JSON manifest on alternativ.co.il and surfaces a standard
// WordPress "update available" notice when a newer version is published.
class Haifa_Staff_V2_Updater {

    private $plugin_slug;
    private $plugin_file;
    private $manifest_url;
    private $zip_url;
    private $current_version;

    public function __construct() {
        $this->plugin_slug     = 'haifa-staff-v2/haifa-staff-v2.php';
        $this->plugin_file     = __FILE__;
        $this->manifest_url    = 'https://alternativ.co.il/plugins/haifa-staff-v2.json';
        $this->zip_url         = 'https://alternativ.co.il/plugins/haifa-staff-v2.zip';
        $this->current_version = HAIFA_STAFF_V2_VERSION;

        add_filter('pre_set_site_transient_update_plugins', [$this, 'check_for_update']);
        add_filter('plugins_api',                           [$this, 'plugin_info'], 10, 3);
        add_filter('update_plugins_alternativ.co.il',      [$this, 'handle_update_check'], 10, 4);
        add_filter('language_attributes',                  [$this, 'maybe_set_ltr_attributes']);
    }

    // Fetch remote manifest (cached 12h).
    private function get_remote_data(): ?array {
        $cache = get_transient('haifa_staff_v2_update_data');
        if ($cache) return $cache;

        $response = wp_remote_get($this->manifest_url, ['timeout' => 10]);
        if (is_wp_error($response)) return null;

        $data = json_decode(wp_remote_retrieve_body($response), true);
        if (empty($data['version'])) return null;

        set_transient('haifa_staff_v2_update_data', $data, 12 * HOUR_IN_SECONDS);
        return $data;
    }

    // On /en/ URLs, switch <html> to dir="ltr" lang="en" so the theme
    // header/footer render correctly in LTR layout.
    public function maybe_set_ltr_attributes($output) {
        $uri = $_SERVER['REQUEST_URI'] ?? '';
        if (preg_match('#(^|/)en/#', $uri)) {
            $output = str_replace('dir="rtl"',    'dir="ltr"', $output);
            $output = str_replace('lang="he-IL"', 'lang="en"', $output);
        }
        return $output;
    }

    // Inject update info into WP transient so the plugins list shows the notice.
    public function check_for_update($transient) {
        if (empty($transient->checked)) return $transient;
        $remote = $this->get_remote_data();
        if (!$remote) return $transient;
        if (version_compare($this->current_version, $remote['version'], '<')) {
            $transient->response[$this->plugin_slug] = (object)[
                'slug'        => 'haifa-staff-v2',
                'plugin'      => $this->plugin_slug,
                'new_version' => $remote['version'],
                'url'         => $remote['details_url'] ?? 'https://alternativ.co.il/plugins/haifa-staff-v2',
                'package'     => $this->zip_url,
            ];
        }
        return $transient;
    }

    // Required for Update URI header — WordPress fires this for non-wp.org plugins.
    public function handle_update_check($update, $plugin_data, $plugin_file, $locales) {
        if ($plugin_file !== $this->plugin_slug) return $update;
        $remote = $this->get_remote_data();
        if (!$remote) return false;
        if (version_compare($this->current_version, $remote['version'], '<')) {
            return (object)[
                'slug'        => 'haifa-staff-v2',
                'plugin'      => $this->plugin_slug,
                'new_version' => $remote['version'],
                'url'         => $remote['details_url'] ?? 'https://alternativ.co.il/plugins/haifa-staff-v2',
                'package'     => $this->zip_url,
            ];
        }
        return false;
    }

    // Provide plugin details popup in wp-admin.
    public function plugin_info($result, $action, $args) {
        if ($action !== 'plugin_information') return $result;
        if (($args->slug ?? '') !== 'haifa-staff-v2') return $result;
        $remote = $this->get_remote_data();
        if (!$remote) return $result;
        return (object)[
            'name'          => 'אוניברסיטת חיפה - אנשי סגל',
            'slug'          => 'haifa-staff-v2',
            'version'       => $remote['version'],
            'author'        => '<a href="https://alternativ.co.il">Benny Mozes</a>',
            'homepage'      => 'https://alternativ.co.il/plugins/haifa-staff-v2',
            'requires'      => $remote['requires_wp']  ?? '6.0',
            'requires_php'  => $remote['requires_php'] ?? '8.0',
            'download_link' => $this->zip_url,
            'sections'      => [
                'description' => $remote['description'] ?? '',
                'changelog'   => $remote['changelog']   ?? '',
            ],
            'icons'   => [
                '1x' => 'https://alternativ.co.il/plugins/haifa-staff/icon-128x128.png',
                '2x' => 'https://alternativ.co.il/plugins/haifa-staff/icon-256x256.png',
            ],
            'banners' => [
                'low'  => 'https://alternativ.co.il/plugins/haifa-staff/banner-772x250.png',
                'high' => 'https://alternativ.co.il/plugins/haifa-staff/banner-772x250.png',
            ],
        ];
    }
}

