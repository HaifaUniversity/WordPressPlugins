    // ── Template loading ──────────────────────────────────────────────────────

    // Intercepts template_redirect and serves single-staff.php when a staff
    // member's slug is found in the URL. Handles both query-var and URL-parsed paths,
    // plus WPML fallback via HAIFA_STAFF_ORIGINAL_URI.
    public function maybe_load_staff_template() {
        $staff_slug    = get_query_var('staff_slug');
        $staff_context = get_query_var('staff_context');

        // Fallback: parse URL directly when page rules fire before our rewrite
        if (empty($staff_slug)) {
            $path  = trim(parse_url($_SERVER['REQUEST_URI'] ?? '', PHP_URL_PATH), '/');
            $parts = explode('/', $path);
            if (count($parts) >= 2) {
                $staff_slug    = array_pop($parts);
                $staff_context = implode('/', $parts);
            }
        }

        // WPML fallback: restore language context from the original URI
        // (WPML rewrites REQUEST_URI before our rewrite fires)
        if (!empty($_SERVER['HAIFA_STAFF_ORIGINAL_URI'])) {
            $orig = trim(parse_url($_SERVER['HAIFA_STAFF_ORIGINAL_URI'], PHP_URL_PATH), '/');
            if (preg_match('#^en/#', $orig)) $staff_context = 'en';
        }

        if (empty($staff_slug)) return;

        $q = new WP_Query(['post_type' => 'staff', 'name' => $staff_slug, 'posts_per_page' => 1]);
        if ($q->have_posts()) {
            global $wp_query;
            $wp_query              = $q;
            $wp_query->is_404      = false;
            $wp_query->is_singular = true;
            $wp_query->is_single   = true;
            set_query_var('staff_context', $staff_context);
            include HAIFA_STAFF_V2_PLUGIN_DIR . 'templates/single-staff.php';
            exit;
        }
    }

    // Runs on 'wp' hook (before title is computed) to set the correct queried
    // object so the <title> tag shows the staff member's name instead of 404.
    public function fix_staff_query_early() {
        $staff_slug = get_query_var('staff_slug');

        if (empty($staff_slug)) {
            $path  = trim(parse_url($_SERVER['REQUEST_URI'] ?? '', PHP_URL_PATH), '/');
            $parts = explode('/', $path);
            if (count($parts) >= 2) $staff_slug = array_pop($parts);
        }

        if (empty($staff_slug)) return;

        $q = new WP_Query(['post_type' => 'staff', 'name' => $staff_slug, 'posts_per_page' => 1]);
        if (!$q->have_posts()) return;

        global $wp_query;
        $wp_query                    = $q;
        $wp_query->is_404            = false;
        $wp_query->is_singular       = true;
        $wp_query->is_single         = true;
        $wp_query->queried_object    = $q->post;
        $wp_query->queried_object_id = $q->post->ID;
    }

