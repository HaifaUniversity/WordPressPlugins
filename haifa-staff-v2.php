    // Builds a hierarchical permalink based on the current page's ancestor path.
    // Used so staff profile URLs mirror the page tree: /faculty/staff/person/
    public function improve_staff_permalinks($link, $post) {
        if (is_admin()) return $link;
        if (!$post || $post->post_type !== 'staff') return $link;
        if (!is_page()) return $link;

        $current_page_id = get_queried_object_id();
        if (!$current_page_id) return $link;

        $ancestors   = array_reverse(get_post_ancestors($current_page_id));
        $ancestors[] = $current_page_id;

        $path_parts = [];
        foreach ($ancestors as $ancestor_id) {
            $ancestor = get_post($ancestor_id);
            if ($ancestor) $path_parts[] = $ancestor->post_name;
        }

        if (empty($path_parts)) return $link;
        return home_url('/' . implode('/', $path_parts) . '/' . $post->post_name . '/');
    }

