}, 99);

// ── Canonical URL ─────────────────────────────────────────────────────────────
// Forces a single canonical for staff profiles and suppresses duplicate
// canonical tags from Yoast / Rank Math / AIOSEO.

add_action('template_redirect', function() {
    $path  = trim(parse_url($_SERVER['REQUEST_URI'] ?? '', PHP_URL_PATH), '/');
    $parts = explode('/', $path);
    if (count($parts) < 2) return;
    $slug = end($parts);
    $post = get_page_by_path($slug, OBJECT, 'staff');
    if (!$post) return;

    $canonical = get_permalink($post->ID);

    remove_action('wp_head', 'rel_canonical');
    add_filter('wpseo_canonical',              function() use ($canonical) { return $canonical; });
    add_filter('rank_math/frontend/canonical', function() use ($canonical) { return $canonical; });
    add_filter('aioseo_canonical_url',         function() use ($canonical) { return $canonical; });

    add_action('wp_head', function() use ($canonical) {
        echo '<link rel="canonical" href="' . esc_url($canonical) . '" />' . "\n";
    }, 1);
});

// Prevent WordPress from redirecting contextual staff URLs (/faculty/staff/person/).
add_filter('redirect_canonical', function($redirect_url, $requested_url) {
    $path  = trim(parse_url($requested_url, PHP_URL_PATH), '/');
    $parts = explode('/', $path);
    if (count($parts) >= 2) {
        $slug = end($parts);
        $post = get_page_by_path($slug, OBJECT, 'staff');
        if ($post) return false;
    }
    return $redirect_url;
}, 10, 2);

