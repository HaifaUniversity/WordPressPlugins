    public function rest_get_staff_list(WP_REST_Request $request): WP_REST_Response {
        $meta_query = [['key' => 'staff_type', 'value' => 'administrative', 'compare' => 'NOT LIKE']];

        if ($request->get_param('staff_type')) {
            $meta_query[] = ['key' => 'staff_type', 'value' => $request->get_param('staff_type'), 'compare' => 'LIKE'];
        }
        if ($request->get_param('staff_academic_type')) {
            $meta_query[] = ['key' => 'staff_academic_type', 'value' => $request->get_param('staff_academic_type'), 'compare' => '='];
        }
        if ($request->get_param('status')) {
            $meta_query[] = ['key' => 'status', 'value' => $request->get_param('status'), 'compare' => '='];
        }

        $args = [
            'post_type'      => 'staff',
            'post_status'    => 'publish',
            'posts_per_page' => -1,
            'meta_query'     => $meta_query,
        ];

        if ($request->get_param('tag')) $args['tag'] = $request->get_param('tag');

        $posts = get_posts($args);
        $data  = array_map([$this, 'haifa_v2_staff_post_to_api_item'], $posts);
        return new WP_REST_Response(array_values(array_filter($data)), 200);
    }

    public function rest_get_staff_single(WP_REST_Request $request): WP_REST_Response {
        $post = get_page_by_path($request->get_param('slug'), OBJECT, 'staff');
        if (!$post || $post->post_status !== 'publish') {
            return new WP_REST_Response(['error' => 'Not found'], 404);
        }
        $staff_type = get_post_meta($post->ID, 'staff_type', true);
        if (is_array($staff_type) && $staff_type === ['administrative']) {
            return new WP_REST_Response(['error' => 'Not found'], 404);
        }
        return new WP_REST_Response($this->haifa_v2_staff_post_to_api_item($post), 200);
    }

    public function rest_get_tags(WP_REST_Request $request): WP_REST_Response {
        $staff_ids = get_posts([
            'post_type'      => 'staff',
            'post_status'    => 'publish',
            'posts_per_page' => -1,
            'fields'         => 'ids',
        ]);

        if (empty($staff_ids)) return new WP_REST_Response([], 200);

        $terms = get_terms([
            'taxonomy'   => 'post_tag',
            'object_ids' => $staff_ids,
            'hide_empty' => true,
        ]);

        if (is_wp_error($terms)) return new WP_REST_Response([], 200);

        $data = array_map(function($t) {
            return ['slug' => $t->slug, 'name' => $t->name, 'count' => $t->count];
        }, $terms);
        return new WP_REST_Response($data, 200);
    }

    // Converts a staff WP_Post into the normalized array structure returned by the REST API.
    private function haifa_v2_staff_post_to_api_item(WP_Post $post): array {
        $id         = $post->ID;
        $staff_type = get_post_meta($id, 'staff_type', true);
        if (is_array($staff_type) && $staff_type === ['administrative']) return [];

        $title_main = get_post_meta($id, 'title_main', true);
        $photo_id   = get_post_meta($id, 'staff_image', true);
        $photo_url  = $photo_id ? wp_get_attachment_image_url((int)$photo_id, 'medium') : '';

        $tags = get_the_terms($id, 'post_tag');
        $tags = (!$tags || is_wp_error($tags)) ? [] : array_map(function($t) {
            return ['slug' => $t->slug, 'name' => $t->name];
        }, $tags);

        return [
            'id'                  => $id,
            'slug'                => $post->post_name,
            'profile_url'         => get_permalink($id),
            'first_name'          => get_post_meta($id, 'first_name',          true),
            'last_name'           => get_post_meta($id, 'last_name',           true),
            'title_main'          => $title_main,
            'title_main_label'    => haifa_v2_title_label((string)$title_main),
            'position'            => get_post_meta($id, 'position',            true),
