    // ── Page title translation ────────────────────────────────────────────────
    // Reads page_title / page_title_en from the Elementor staff grid widget
    // settings and injects the correct language version into the <title> tag.

    public function maybe_translate_page_title($title) {
        $page_id = get_queried_object_id();
        if (!$page_id) return $title;

        $elementor_data = get_post_meta($page_id, '_elementor_data', true);
        if (empty($elementor_data)) return $title;

        $data = json_decode($elementor_data, true);
        if (!is_array($data)) return $title;

        $uri      = $_SERVER['REQUEST_URI'] ?? '';
        $is_en    = (bool) preg_match('#(^|/)en/#', $uri);
        $key      = $is_en ? 'page_title_en' : 'page_title';
        $new_title = $this->find_elementor_widget_setting($data, 'haifa_staff_grid', $key);
        if (!empty($new_title)) $title['title'] = $new_title;

        return $title;
    }

    // Recursively search Elementor elements tree for a specific widget setting.
    private function find_elementor_widget_setting($elements, $widget_name, $setting_key) {
        foreach ($elements as $element) {
            if (isset($element['widgetType']) && $element['widgetType'] === $widget_name) {
                return $element['settings'][$setting_key] ?? '';
            }
            if (!empty($element['elements'])) {
                $found = $this->find_elementor_widget_setting($element['elements'], $widget_name, $setting_key);
                if ($found) return $found;
            }
        }
        return '';
    }

