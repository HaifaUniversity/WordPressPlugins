    // ── CPT ───────────────────────────────────────────────────────────────────

    public function register_cpt() {
        if (post_type_exists($this->post_type)) return;

        $cpt_json = HAIFA_STAFF_V2_PLUGIN_DIR . 'assets/acf-cpt.json';
        if (!file_exists($cpt_json)) return;

        $settings = json_decode(file_get_contents($cpt_json), true);
        if (is_array($settings) && isset($settings['post_type']) && $settings['post_type'] === $this->post_type) {
            if (isset($settings['labels'], $settings['supports'])) {
                register_post_type($this->post_type, $settings);
            }
        }

        // Register ACF field group locally so Elementor dynamic tags work
        // even without ACF field groups saved in the database.
        // Location rules are stripped so the group never renders on edit screens.
        if (function_exists('acf_add_local_field_group')) {
            $fields_json = HAIFA_STAFF_V2_PLUGIN_DIR . 'assets/acf-staff-fields.json';
            if (file_exists($fields_json)) {
                $field_group = json_decode(file_get_contents($fields_json), true);
                if ($field_group) {
                    $group = isset($field_group[0]) ? $field_group[0] : $field_group;
                    $group['location'] = [];
                    acf_add_local_field_group($group);
                }
            }
        }
    }

