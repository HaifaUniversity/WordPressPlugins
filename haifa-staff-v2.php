});

// ── [acf_label] shortcode override ───────────────────────────────────────────
// Supports old Elementor templates that use [acf_label] for academic_degree_main,
// academic_degree_other, and position without requiring template changes.
// Priority 20 — runs after ACF registers its own shortcode.

add_action('init', function() {
    $handled_fields = ['academic_degree_main', 'academic_degree_other', 'position'];
    $degree_labels  = [
        'professor'           => 'פרופסור מן המניין',
        'associate_professor' => 'פרופסור חבר',
        'professor_emeritus'  => 'פרופסור אמריטוס',
        'senior_lecturer'     => 'מרצה בכיר',
        'other'               => 'אחר',
    ];

    global $shortcode_tags;
    $original_acf_handler = $shortcode_tags['acf_label'] ?? null;

    remove_shortcode('acf_label');

    add_shortcode('acf_label', function($atts) use ($handled_fields, $degree_labels, $original_acf_handler) {
        $atts = shortcode_atts(['name' => ''], $atts);
        $name = $atts['name'];

        if (!in_array($name, $handled_fields, true)) {
            return $original_acf_handler ? call_user_func($original_acf_handler, $atts) : '';
        }

        $post_id = get_the_ID();
        if (!$post_id) return '';

        if ($name === 'academic_degree_main') {
            $value = get_post_meta($post_id, 'academic_degree_main', true);
            return $degree_labels[$value] ?? '';
        }
        if ($name === 'academic_degree_other') {
            $degree = get_post_meta($post_id, 'academic_degree_main', true);
            if ($degree !== 'other') return '';
            return esc_html(get_post_meta($post_id, 'academic_degree_other', true));
        }
        if ($name === 'position') {
            return esc_html(get_post_meta($post_id, 'position', true));
        }
        return '';
    });
}, 20);

